export function filterAdminNavItems(
  items: AdminNavItem[],
  flags: { opsEnabled: boolean },
): AdminNavItem[] {
  return items.filter((item) => {
    if (item.opsOnly && !flags.opsEnabled) return false;
    return true;
  });
}

export function isAdminItemActive(
  item: AdminNavItem,
  pathname: string,
  basePath: string,
): boolean {
  if (item.path === "") {
    return pathname === basePath || pathname === `${basePath}/`;
  }
  if (item.activePaths.length > 0) {
    return item.activePaths.some((p) => pathname.startsWith(`${basePath}${p}`));
  }
  return pathname.startsWith(`${basePath}${item.path}`);
}
