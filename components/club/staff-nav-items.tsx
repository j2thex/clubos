export function filterStaffNavItems(
  items: StaffNavItem[],
  flags: { spinEnabled: boolean; operationsEnabled: boolean; qeboEnabled: boolean },
): StaffNavItem[] {
  return items.filter((item) => {
    if (!flags.spinEnabled && item.labelKey === "nav.bonuses") return false;
    if (!flags.operationsEnabled && item.labelKey === "nav.operations") return false;
    if (!flags.qeboEnabled && STAFF_QEBO_LABELS.has(item.labelKey)) return false;
    return true;
  });
}

export function isStaffItemActive(
  item: StaffNavItem,
  pathname: string,
  basePath: string,
): boolean {
  if (item.path === "") {
    return pathname === basePath || pathname === `${basePath}/`;
  }
  return pathname.startsWith(`${basePath}${item.path}`);
}
